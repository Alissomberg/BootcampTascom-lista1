// 2. Calcular as raízes de uma equação de segundo grau ax² + bx + c

// Dada uma equação aleatória: x² + 2x - 3, temos:
a = 1
b = 2
c = -3
delta = b**2 - 4*a*c
raiz1 = (-b + delta**(1/2))/2*a
raiz2 = (-b - delta**(1/2))/2*a

console.log(`As raízes da equação x² -6x + 9 são:\nRaiz 1: ${raiz1}\nRaiz 2: ${raiz2}`)